    private void AGREGARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AGREGARActionPerformed
        // Nuevo registro
        try {
            reg.setIdFormaPago(Integer.parseInt(this.id_forma_pago.getText()));
            reg.setDescripcion(this.descripcion.getText());
            reg.setAccion("new");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error" + e);
        }
        cn = new conectarBD();
        String res = cn.ProcedimientoFormaPago(reg.getIdFormaPago(), reg.getDescripcion(), reg.getAccion());
        JOptionPane.showMessageDialog(null, "" + res);

        actualizar_pendientes();
        Borrar();
        this.ELIMINAR.setEnabled(true);
        this.ACTUALIZAR.setEnabled(true);
    }//GEN-LAST:event_AGREGARActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        //Lanzar datos a panel ingreso de Datos

        int fila = this.jTable1.getSelectedRow();
        try {
            reg.setIdFormaPago(Integer.parseInt(jTable1.getValueAt(fila, 0).toString()));
            reg.setDescripcion(jTable1.getValueAt(fila, 1).toString());
            reg.setAccion("query");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error" + e);
        }

        id_forma_pago.setText(String.valueOf(reg.getIdFormaPago()));
        descripcion.setText(reg.getDescripcion());

        this.ELIMINAR.setEnabled(true);
        this.ACTUALIZAR.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

